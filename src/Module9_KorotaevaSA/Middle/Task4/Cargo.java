package Module9_KorotaevaSA.Middle.Task4;

public class Cargo {
    private String name;
    private int weight;

    public Cargo(String name, int weight) {
        this.name = name;
        this.weight = weight;
    }

    public int getWeight() {
        return weight;
    }

    public String getName() {
        return name;
    }
}
